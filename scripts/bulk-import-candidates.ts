async function main() {
  console.log(`\nBulk importing ${CANDIDATES.length} candidates into crm_candidates...\n`)

  // Fetch all existing full_names for dedup check
  const { data: existing, error: fetchErr } = await supabase
    .from('crm_candidates')
    .select('full_name')

  if (fetchErr) {
    console.error('Failed to fetch existing candidates:', fetchErr.message)
    process.exit(1)
  }

  const existingNames = new Set((existing ?? []).map((r: { full_name: string }) => r.full_name.toLowerCase().trim()))

  const toInsert = []
  const skipped: string[] = []

  for (const c of CANDIDATES) {
    if (existingNames.has(c.full_name.toLowerCase().trim())) {
      skipped.push(c.full_name)
    } else {
      toInsert.push(c)
    }
  }

  if (skipped.length > 0) {
    console.log(`Skipping ${skipped.length} duplicate(s):`)
    skipped.forEach(n => console.log(`  ↷ ${n}`))
    console.log()
  }

  if (toInsert.length === 0) {
    console.log('Nothing to insert — all candidates already exist.')
    return
  }

  const { error: insertErr } = await supabase
    .from('crm_candidates')
    .insert(toInsert)

  if (insertErr) {
    console.error('Insert failed:', insertErr.message)
    process.exit(1)
  }

  console.log(`✓ Inserted ${toInsert.length} candidate(s):`)
  toInsert.forEach(c => console.log(`  + ${c.full_name}`))
  console.log(`\nDone. ${toInsert.length} inserted, ${skipped.length} skipped.\n`)
}

main()
